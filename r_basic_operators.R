#数学运算符
# 优先级   符号    含义
#   1       ()     括号
#   2       ^      乘方
#   3       %%     整除求余
#           %/%    整除
#   4       *      乘法
#           /      除法
#   5       +      加法
#           -      减法
1 + 2 * 3    #7
(1 + 2) * 3  #9
3 / 4        #0.75
4.4 / 0.5    #8.8
3 %/% 4      #0
4.4 %/% 0.5  #8
10 %% 3      #1
10 %% 2      #0
3.2 %% 2     #1.2
5 ^ 2        #25
5 ^ 3        #125
25 ^ 0.5     #5
2 ^ 2.5      #5.656854


#逻辑运算符
# 运算符      描述
#   &         逻辑与,作用于向量的每一个元素
#   |         逻辑或,作用于向量的每一个元素
#   !         逻辑非,作用于向量的每一个元素
#   &&        逻辑与,作用于向量的第一个元素
#   ||        逻辑或,作用于向量的第一个元素
m <- c(3,0,TRUE,2+2i)
n <- c(1,3,TRUE,2+3i)
m&n          #TRUE FALSE  TRUE  TRUE
m|n          #TRUE TRUE TRUE TRUE
!m           #FALSE  TRUE FALSE FALSE
#m&&n
#m||n
5 && 3       #TRUE
5 && 0       #FALSE

